export function detectLang(stored: string | null, navigatorLang: string | undefined): Lang {
  if (stored === 'en' || stored === 'pt') return stored;
  if (navigatorLang && navigatorLang.toLowerCase().startsWith('pt')) return 'pt';
  return 'en';
}
